// Command gen-top-ports regenerates the tail of the Top1000 port list
// from IANA's registry.
//
// The top100 prefix is hand-curated in internal/portscan/top.go (nmap
// frequency ordering) and preserved verbatim by this generator. The
// remaining 900 slots are filled with IANA-assigned TCP ports in
// numeric order, skipping any already in top100.
//
// Usage:
//
//	gen-top-ports -in data/iana-service-names-port-numbers.csv \
//	              -out internal/portscan/top.go
//
// Rationale: nmap-services has richer frequency data but ships under
// a copyleft license incompatible with scry's MIT. See scry-plan.md
// §10 #23. The resulting top1000 is less accurate than nmap's for
// ports 101–1000 but is license-clean and reproducible.
package main

